/// Create a blank `PriceCircuit` with non-zero but otherwise arbitrary values.
///
/// Used for `Groth16::setup` вЂ” the R1CS structure must be identical to what
/// the real prover will use, but witness values can be anything valid.
fn blank_price_circuit() -> PriceCircuit {
    let public = CircuitPublicInputs {
        symbol_hash:   [1u8; 32],
        price:         1_000_00000000u128, // $1000 at 8 decimals
        timestamp:     1_000_000_000u64,
        vk_hash:       [2u8; 32],
        notary_pubkey: [3u8; 33],
    };
    let tls_response = b"blank-circuit-tls-witness-for-setup";
    let private = CircuitPrivateInputs::from_tls(tls_response, &public)
        .expect("blank circuit witness derivation must succeed");
    PriceCircuit::new(public, private)
}

