//! Groth16 prover for ZK price proofs вЂ” ZEP-012 end-to-end.
//!
//! # Flow
//!
//! ```text
//! PriceProver::from_pk_bytes(proving_key_bytes)
//!   в†“
//! prover.prove(public_inputs, tls_response, notary_attestation)
//!   в†“  (witness derivation в†’ Groth16::prove в†’ serialize proof bytes)
//! ZkPriceProof { groth16_a, groth16_b, groth16_c, public_inputs }
//! ```
//!
//! # Proving key bootstrap
//!
//! The proving key is the large (~50 MB) output of the ZEP-012 trusted setup
//! ceremony.  `PriceProver::from_pk_bytes` accepts the compressed serialisation
//! produced by `Groth16::<Bn254>::setup`.
//!
//! For development / testing, `PriceProver::ephemeral` generates a fresh
//! proving key from a random trapdoor вЂ” valid proofs but **not safe for
//! production** (the trapdoor is discarded but was briefly in memory).
//!
//! # Proof wire format
//!
//! The proof bytes written into `ZkPriceProof` are the *uncompressed* flat
//! layout expected by the verifier:
//! ```text
//! groth16_a  (64 bytes) вЂ” G1 affine, uncompressed
//! groth16_b (128 bytes) вЂ” G2 affine, uncompressed
//! groth16_c  (64 bytes) вЂ” G1 affine, uncompressed
//! ```
//! This matches [`crate::proof::ZkPriceProof::proof_bytes_canonical`] and
//! the on-chain `ZbxGroth16Verifier.sol` ABI.

use crate::{
    circuit::{CircuitPrivateInputs, CircuitPublicInputs, PriceCircuit},
    notary::NotaryAttestation,
    proof::{ZkPriceProof, ZkPublicInputs},
    verifier::ZkVerifyingKey,
};

use ark_bn254::Bn254;
use ark_groth16::{Groth16, ProvingKey};
use ark_serialize::{CanonicalDeserialize, CanonicalSerialize, Compress, Validate};
use ark_ff::One;
use ark_snark::{CircuitSpecificSetupSNARK, SNARK};
use rand::rngs::OsRng;
