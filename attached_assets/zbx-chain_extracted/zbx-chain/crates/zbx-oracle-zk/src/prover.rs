#[derive(Debug, Error)]
pub enum ProverError {
    #[error("invalid proving key bytes: {0}")]
    InvalidProvingKey(String),

    #[error("witness derivation failed вЂ” tls_digest is zero (degenerate TLS response)")]
    DegenerateTlsDigest,

    #[error("pre-proof validation failed: circuit is not satisfied")]
    UnsatisfiedCircuit,

    #[error("Groth16 proving failed: {0}")]
    ProvingFailed(String),

    #[error("proof serialization failed: {0}")]
    SerializationFailed(String),

    #[error("trusted setup (ceremony) failed: {0}")]
    SetupFailed(String),
}

