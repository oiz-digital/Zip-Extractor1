/// Groth16 prover for the ZK price circuit.
///
/// One `PriceProver` instance per feed (each feed has its own proving key from
/// the trusted setup ceremony).  Creating a prover is expensive (deserialises
/// the proving key); reuse it across multiple proof requests.
pub struct PriceProver {
    pk: ProvingKey<Bn254>,
}

impl PriceProver {
    // в”Ђв”Ђ Constructors в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

    /// Load a prover from a serialised proving key.
    ///
    /// `pk_bytes` is the compressed `arkworks` serialisation of
    /// `ark_groth16::ProvingKey<Bn254>` as produced by the ZEP-012
    /// trusted setup ceremony.
    ///
    /// # Errors
    /// Returns [`ProverError::InvalidProvingKey`] if the bytes are malformed.
    pub fn from_pk_bytes(pk_bytes: &[u8]) -> Result<Self, ProverError> {
        let pk = ProvingKey::<Bn254>::deserialize_with_mode(
            pk_bytes,
            Compress::Yes,
            Validate::Yes,
        )
        .map_err(|e| ProverError::InvalidProvingKey(e.to_string()))?;
        Ok(Self { pk })
    }

    /// Create a `PriceProver` with a **fresh random trapdoor**.
    ///
    /// вљ пёЏ  **Development / testing only.**  The resulting proofs are
    /// cryptographically valid but the proving key was not generated by a
    /// trusted-setup ceremony вЂ” anyone who runs this function could generate
    /// fraudulent proofs.  Never use in production.
    ///
    /// Also returns the verifying key bytes so tests can register the VK with
    /// [`crate::verifier::ZkOracleVerifier`].
    pub fn ephemeral() -> Result<(Self, Vec<u8>), ProverError> {
        // Use a blank circuit as the circuit shape for setup вЂ” the R1CS
        // structure (number of variables, constraints) is the same regardless
        // of witness values.
        let blank_circuit = blank_price_circuit();

        let (pk, vk) = Groth16::<Bn254>::setup(blank_circuit, &mut OsRng)
            .map_err(|e| ProverError::SetupFailed(e.to_string()))?;

        // Serialise the verifying key for registration with ZkOracleVerifier.
        let mut vk_bytes = Vec::new();
        vk.serialize_compressed(&mut vk_bytes)
            .map_err(|e| ProverError::SerializationFailed(e.to_string()))?;

        Ok((Self { pk }, vk_bytes))
    }

    /// Derive the verifying key from the loaded proving key.
    ///
    /// Used to build a [`ZkVerifyingKey`] for registration with
    /// [`crate::verifier::ZkOracleVerifier`].
    pub fn verifying_key_bytes(&self) -> Result<Vec<u8>, ProverError> {
        let vk = self.pk.vk.clone();
        let mut buf = Vec::new();
        vk.serialize_compressed(&mut buf)
            .map_err(|e| ProverError::SerializationFailed(e.to_string()))?;
        Ok(buf)
    }

    // в”Ђв”Ђ Main prove method в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

    /// Generate a ZK price proof.
    ///
    /// # Arguments
    /// * `public`  вЂ” circuit public inputs (symbol_hash, price, timestamp, vk_hash, notary_pubkey).
    /// * `tls_response` вЂ” raw TLS HTTP response bytes from the CEX API endpoint.
    /// * `attest` вЂ” notary attestation (used to populate `ZkPublicInputs.notary_pubkey`).
    ///
    /// # Steps
    /// 1. Derive private witnesses via [`CircuitPrivateInputs::from_tls`].
    /// 2. Run `PriceCircuit::is_satisfied()` pre-check.
    /// 3. Call `Groth16::<Bn254>::prove` to generate the real BN254 proof.
    /// 4. Deserialise proof points and pack into [`ZkPriceProof`] wire format.
    ///
    /// # Errors
    /// * [`ProverError::DegenerateTlsDigest`] вЂ” degenerate (zero) TLS hash.
    /// * [`ProverError::UnsatisfiedCircuit`] вЂ” pre-check failed.
    /// * [`ProverError::ProvingFailed`] вЂ” arkworks Groth16 error.
    pub fn prove(
        &self,
        public:       CircuitPublicInputs,
        tls_response: &[u8],
        attest:       &NotaryAttestation,
    ) -> Result<ZkPriceProof, ProverError> {
        // 1. Derive private witnesses.
        let private = CircuitPrivateInputs::from_tls(tls_response, &public)
            .ok_or(ProverError::DegenerateTlsDigest)?;

        let circuit = PriceCircuit::new(public.clone(), private);

        // 2. Pre-flight structural check.
        if !circuit.is_satisfied() {
            return Err(ProverError::UnsatisfiedCircuit);
        }

        // 3. Generate Groth16 proof (clone circuit for the move into prove).
        let proof = Groth16::<Bn254>::prove(&self.pk, circuit, &mut OsRng)
            .map_err(|e| ProverError::ProvingFailed(e.to_string()))?;

        // 4. Serialise proof points into the ZkPriceProof wire format.
        //
        // The verifier expects *uncompressed* G1/G2 affine points in the
        // flat layout: pi_a (64 B) || pi_b (128 B) || pi_c (64 B).
        let mut pi_a_buf = Vec::new();
        let mut pi_b_buf = Vec::new();
        let mut pi_c_buf = Vec::new();
        proof.a.serialize_uncompressed(&mut pi_a_buf)
            .map_err(|e| ProverError::SerializationFailed(format!("pi_a: {e}")))?;
        proof.b.serialize_uncompressed(&mut pi_b_buf)
            .map_err(|e| ProverError::SerializationFailed(format!("pi_b: {e}")))?;
        proof.c.serialize_uncompressed(&mut pi_c_buf)
            .map_err(|e| ProverError::SerializationFailed(format!("pi_c: {e}")))?;

        if pi_a_buf.len() != 64 || pi_b_buf.len() != 128 || pi_c_buf.len() != 64 {
            return Err(ProverError::SerializationFailed(format!(
                "unexpected point sizes: pi_a={}, pi_b={}, pi_c={}",
                pi_a_buf.len(), pi_b_buf.len(), pi_c_buf.len()
            )));
        }

        let mut groth16_a  = [0u8; 64];
        let mut groth16_b  = [0u8; 128];
        let mut groth16_c  = [0u8; 64];
        groth16_a.copy_from_slice(&pi_a_buf);
        groth16_b.copy_from_slice(&pi_b_buf);
        groth16_c.copy_from_slice(&pi_c_buf);

        // 5. Build the matching public inputs struct for the wire type.
        let pub_inputs = ZkPublicInputs {
            symbol_hash:   public.symbol_hash,
            price:         public.price as i128,
            timestamp:     public.timestamp,
            vk_hash:       public.vk_hash,
            notary_pubkey: attest.notary_pubkey,
        };

        Ok(ZkPriceProof { groth16_a, groth16_b, groth16_c, public_inputs: pub_inputs })
    }
}

