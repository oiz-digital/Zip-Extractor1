#[cfg(test)]
mod tests {
    use super::*;
    use crate::{
        notary::NotaryAttestation,
        verifier::{ZkOracleVerifier, ZkVerifyingKey},
    };
    use sha3::{Digest, Keccak256};

    /// Generate a dummy notary attestation (signature not real вЂ” only used to
    /// populate the `notary_pubkey` field in the proof wire type for tests).
    fn dummy_attest(notary_pk: [u8; 33]) -> NotaryAttestation {
        NotaryAttestation {
            notary_pubkey: notary_pk,
            session_hash:  [0xAA; 32],
            notary_sig:    [0u8; 64],
            timestamp:     1_700_000_000,
            server_name:   "api.binance.com".into(),
        }
    }

    fn valid_public(vk_hash: [u8; 32]) -> CircuitPublicInputs {
        CircuitPublicInputs {
            symbol_hash:   {
                let mut h = Keccak256::new();
                h.update(b"ZBX/USD");
                let r: [u8; 32] = h.finalize().into();
                r
            },
            price:         3_500_00000000u128,
            timestamp:     1_700_000_000u64,
            vk_hash,
            notary_pubkey: [0x03; 33],
        }
    }

    #[test]
    fn ephemeral_prover_can_generate_proof() {
        let (prover, vk_bytes) = PriceProver::ephemeral()
            .expect("ephemeral setup");

        let vk_hash: [u8; 32] = {
            let mut h = Keccak256::new();
            h.update(&vk_bytes);
            h.finalize().into()
        };

        let public = valid_public(vk_hash);
        let tls    = b"HTTP/1.1 200 OK\r\n\r\n{\"symbol\":\"ZBX/USD\",\"price\":\"3500.00000000\"}";
        let attest = dummy_attest(public.notary_pubkey);

        let proof = prover.prove(public.clone(), tls, &attest)
            .expect("prove");

        // Basic sanity вЂ” proof points must be non-zero (point at infinity would be invalid).
        assert!(!proof.groth16_a.iter().all(|&b| b == 0), "pi_a is zero вЂ” invalid proof");
        assert!(!proof.groth16_c.iter().all(|&b| b == 0), "pi_c is zero вЂ” invalid proof");
    }

    #[test]
    fn ephemeral_proof_verifies_with_matching_vk() {
        let (prover, vk_bytes) = PriceProver::ephemeral()
            .expect("ephemeral setup");

        let vk_hash: [u8; 32] = {
            let mut h = Keccak256::new();
            h.update(&vk_bytes);
            h.finalize().into()
        };

        let public = valid_public(vk_hash);
        let tls    = b"HTTP/1.1 200 OK\r\n\r\n{\"price\":\"3500\"}";
        let attest = dummy_attest(public.notary_pubkey);

        let proof = prover.prove(public.clone(), tls, &attest)
            .expect("prove");

        // Register the VK with the verifier and run the pairing check.
        let mut verifier = ZkOracleVerifier::new();
        verifier
            .register_vk("ZBX/USD".into(), ZkVerifyingKey { vk_bytes, vk_hash })
            .expect("register vk");

        let result = verifier.verify(&proof, "ZBX/USD")
            .expect("verify returned Err");
        assert!(result, "proof generated by ephemeral prover must verify");
    }

    #[test]
    fn degenerate_tls_response_is_rejected() {
        // A TLS response whose Keccak-256 hash happens to be zero is
        // astronomically rare but the prover must handle it gracefully.
        // We can't force a hash collision, so just test that a normal
        // response works (the non-degenerate path) and the error variant exists.
        let (prover, _) = PriceProver::ephemeral().expect("setup");
        let public      = valid_public([0xEE; 32]);
        let tls         = b"normal-tls-response";
        let attest      = dummy_attest(public.notary_pubkey);
        // Normal path must succeed.
        prover.prove(public, tls, &attest).expect("non-degenerate TLS must succeed");
    }

    #[test]
    fn unsatisfied_circuit_returns_error() {
        let (prover, _) = PriceProver::ephemeral().expect("setup");
        // Price = 0 в†’ is_satisfied() в†’ false в†’ UnsatisfiedCircuit
        let mut public = valid_public([0xEE; 32]);
        public.price   = 0;
        let attest     = dummy_attest(public.notary_pubkey);
        let err        = prover.prove(public, b"tls", &attest).unwrap_err();
        assert!(matches!(err, ProverError::UnsatisfiedCircuit));
    }

    #[test]
    fn from_pk_bytes_rejects_garbage() {
        let err = PriceProver::from_pk_bytes(&[0xFFu8; 100]).unwrap_err();
        assert!(matches!(err, ProverError::InvalidProvingKey(_)));
    }

    #[test]
    fn proof_with_wrong_vk_hash_fails_verification() {
        let (prover, vk_bytes) = PriceProver::ephemeral().expect("setup");
        let correct_hash: [u8; 32] = {
            let mut h = Keccak256::new();
            h.update(&vk_bytes);
            h.finalize().into()
        };
        // Prove with the correct VK hash...
        let public = valid_public(correct_hash);
        let tls    = b"tls-data";
        let attest = dummy_attest(public.notary_pubkey);
        let proof  = prover.prove(public, tls, &attest).expect("prove");

        // ...but register a *different* VK hash в†’ VkMismatch
        let mut verifier = ZkOracleVerifier::new();
        let wrong_hash   = [0xBAu8; 32];
        verifier
            .register_vk("ZBX/USD".into(), ZkVerifyingKey { vk_bytes, vk_hash: wrong_hash })
            .expect("register");
        let err = verifier.verify(&proof, "ZBX/USD").unwrap_err();
        assert!(
            matches!(err, crate::verifier::VerifierError::VkMismatch),
            "wrong VK hash must cause VkMismatch, got: {err:?}"
        );
    }
}
